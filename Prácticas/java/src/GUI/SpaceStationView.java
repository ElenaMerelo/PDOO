    void setSpaceStation(SpaceStationToUI s){
        weapons_panel.removeAll();
        shields_panel.removeAll();
        hangar_panel.removeAll();
        
        station_name.setText("Estaci√≥n espacial " + s.getName()); 
        ammoPower.setText(Float.toString(s.getAmmoPower()));
        fuelUnits.setText(Float.toString(s.getFuelUnits()));
        nMedals.setText(Integer.toString(s.getnMedals()));
        shieldPower.setText(Float.toString(s.getShieldPower()));
        
        hv= new HangarView();
        if(s.getHangar() != null){
            hv.setHangar(s.getHangar());
            hangar_panel.add(hv);
        }
        
        WeaponView wv;
        for(WeaponToUI w: s.getWeapons()){
            wv= new WeaponView();
            wv.setWeapon(w);
            weapons_panel.add(wv);
        }
        
        ShieldBoosterView sv;
        for(ShieldToUI sh: s.getShieldBoosters()){
            sv= new ShieldBoosterView();
            sv.setShieldBooster(sh);
            shields_panel.add(sv);
        }
        
        if( MainView.controller.getState() == GameState.INIT || MainView.controller.getState() == GameState.AFTERCOMBAT ){
            equip.setEnabled(true);
            discard.setEnabled(true);
            discard_hangar.setEnabled(true);
        }
        else{
            equip.setEnabled(false);
            discard.setEnabled(false);
            discard_hangar.setEnabled(false);
        }
        
        reanimar_button.setEnabled(false);
        if(MainView.controller.spaceStationIsDead()){
            reanimar_button.setEnabled(true);
            this.setBackground(Color.RED);
        }
        
        repaint();
        revalidate();
    }
    
    ArrayList<Integer> getSelectedWeapons(){
        ArrayList<Integer> selected_weapons= new ArrayList();
        int i= 0;
        for (Component c : weapons_panel.getComponents()){
            if(((WeaponView) c).isSelected())
                selected_weapons.add(i);
           
            i++;
        }
        return selected_weapons;
    }
    
    ArrayList<Integer> getSelectedShields(){
        ArrayList<Integer> selected_shields= new ArrayList();
        int i= 0;
        for(Component c: shields_panel.getComponents()){
            if(((ShieldBoosterView) c).isSelected())
                selected_shields.add(i); 
            
            i++;
        }  
        return selected_shields;
    }

